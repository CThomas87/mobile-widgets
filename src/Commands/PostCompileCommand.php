            $content = $result;
        }

        $result = $this->insertBeforeMarker(
            $content,
            '/* End PBXNativeTarget section */',
            "\t\t{$ids['target']} /* {$targetName} */ = {\n"
            ."\t\t\tisa = PBXNativeTarget;\n"
            ."\t\t\tbuildConfigurationList = {$ids['configList']} /* Build configuration list for PBXNativeTarget \"{$targetName}\" */;\n"
            ."\t\t\tbuildPhases = (\n"
            ."\t\t\t\t{$ids['sourcesPhase']} /* Sources */,\n"
            ."\t\t\t\t{$ids['frameworksPhase']} /* Frameworks */,\n"
            ."\t\t\t\t{$ids['resourcesPhase']} /* Resources */,\n"
            ."\t\t\t);\n"
            ."\t\t\tbuildRules = (\n"
            ."\t\t\t);\n"
            ."\t\t\tdependencies = (\n"
            ."\t\t\t);\n"
            ."\t\t\tfileSystemSynchronizedGroups = (\n"
            ."\t\t\t\t{$ids['syncGroup']} /* WidgetsExtension */,\n"
            ."\t\t\t);\n"
            ."\t\t\tname = {$targetName};\n"
            ."\t\t\tpackageProductDependencies = (\n"
            ."\t\t\t);\n"
            ."\t\t\tproductName = {$targetName};\n"
            ."\t\t\tproductReference = {$ids['fileRef']} /* {$appexName} */;\n"
            ."\t\t\tproductType = \"com.apple.product-type.app-extension\";\n"
            ."\t\t};\n"
        );

        if ($result === null) {
            return null;
        }

        $content = $result;

        $result = $this->addLineToProjectTargets(
            $content,
            "\t\t\t\t{$ids['target']} /* {$targetName} */,\n"
        );
        if ($result !== null) {
            $content = $result;
        }

        $result = $this->insertBeforeMarker(
            $content,
            '/* End PBXResourcesBuildPhase section */',
            "\t\t{$ids['resourcesPhase']} /* Resources */ = {\n"
            ."\t\t\tisa = PBXResourcesBuildPhase;\n"
            ."\t\t\tbuildActionMask = 2147483647;\n"
            ."\t\t\tfiles = (\n"
            ."\t\t\t);\n"
            ."\t\t\trunOnlyForDeploymentPostprocessing = 0;\n"
            ."\t\t};\n"
        );

        if ($result === null) {
            return null;
        }

        $content = $result;

        $result = $this->insertBeforeMarker(
            $content,
            '/* End PBXSourcesBuildPhase section */',
            "\t\t{$ids['sourcesPhase']} /* Sources */ = {\n"
            ."\t\t\tisa = PBXSourcesBuildPhase;\n"
            ."\t\t\tbuildActionMask = 2147483647;\n"
            ."\t\t\tfiles = (\n"
