            $content,
            $mainTargetName,
            'buildPhases',
            "\t\t\t\t{$ids['copyPhase']} /* Embed App Extensions */,\n"
        );
        if ($result !== null) {
            $content = $result;
        }

        $result = $this->addLineToNamedTargetList(
            $content,
            $mainTargetName,
            'dependencies',
            "\t\t\t\t{$ids['targetDependency']} /* PBXTargetDependency */,\n"
        );
        if ($result !== null) {
            $content = $result;
        }

        $result = $this->insertBeforeMarker(
            $content,
            '/* End XCBuildConfiguration section */',
            "\t\t{$ids['debugConfig']} /* Debug */ = {\n"
            ."\t\t\tisa = XCBuildConfiguration;\n"
            ."\t\t\tbuildSettings = {\n"
            ."\t\t\t\tAPPLICATION_EXTENSION_API_ONLY = YES;\n"
            ."\t\t\t\tCODE_SIGN_ENTITLEMENTS = WidgetsExtension/NativePHPWidgets.entitlements;\n"
            ."\t\t\t\tCODE_SIGN_STYLE = Automatic;\n"
            ."\t\t\t\tCURRENT_PROJECT_VERSION = 1;\n"
            ."\t\t\t\tGENERATE_INFOPLIST_FILE = YES;\n"
            ."\t\t\t\tINFOPLIST_FILE = WidgetsExtension/Info.plist;\n"
            ."\t\t\t\tIPHONEOS_DEPLOYMENT_TARGET = 16.0;\n"
            ."\t\t\t\tLD_RUNPATH_SEARCH_PATHS = (\n"
            ."\t\t\t\t\t\"$(inherited)\",\n"
            ."\t\t\t\t\t\"@executable_path/Frameworks\",\n"
            ."\t\t\t\t\t\"@executable_path/../../Frameworks\",\n"
            ."\t\t\t\t);\n"
            ."\t\t\t\tMARKETING_VERSION = 1.0;\n"
            ."\t\t\t\tNATIVEPHP_WIDGETS_APP_GROUP = {$appGroup};\n"
            ."\t\t\t\tPRODUCT_BUNDLE_IDENTIFIER = {$bundleId};\n"
            ."\t\t\t\tPRODUCT_NAME = \"$(TARGET_NAME)\";\n"
            ."\t\t\t\tSKIP_INSTALL = YES;\n"
            ."\t\t\t\tSWIFT_EMIT_LOC_STRINGS = YES;\n"
            ."\t\t\t\tSWIFT_VERSION = 5.0;\n"
            ."\t\t\t\tTARGETED_DEVICE_FAMILY = \"1,2\";\n"
            ."\t\t\t\tWIDGET_EXTENSION_KIND = NativePHPWidget;\n"
            ."\t\t\t};\n"
            ."\t\t\tname = Debug;\n"
            ."\t\t};\n"
            ."\t\t{$ids['releaseConfig']} /* Release */ = {\n"
            ."\t\t\tisa = XCBuildConfiguration;\n"
            ."\t\t\tbuildSettings = {\n"
            ."\t\t\t\tAPPLICATION_EXTENSION_API_ONLY = YES;\n"
            ."\t\t\t\tCODE_SIGN_ENTITLEMENTS = WidgetsExtension/NativePHPWidgets.entitlements;\n"
            ."\t\t\t\tCODE_SIGN_STYLE = Automatic;\n"
            ."\t\t\t\tCURRENT_PROJECT_VERSION = 1;\n"
            ."\t\t\t\tGENERATE_INFOPLIST_FILE = YES;\n"
            ."\t\t\t\tINFOPLIST_FILE = WidgetsExtension/Info.plist;\n"
            ."\t\t\t\tIPHONEOS_DEPLOYMENT_TARGET = 16.0;\n"
            ."\t\t\t\tLD_RUNPATH_SEARCH_PATHS = (\n"
            ."\t\t\t\t\t\"$(inherited)\",\n"
            ."\t\t\t\t\t\"@executable_path/Frameworks\",\n"
            ."\t\t\t\t\t\"@executable_path/../../Frameworks\",\n"
            ."\t\t\t\t);\n"
            ."\t\t\t\tMARKETING_VERSION = 1.0;\n"
            ."\t\t\t\tNATIVEPHP_WIDGETS_APP_GROUP = {$appGroup};\n"
            ."\t\t\t\tPRODUCT_BUNDLE_IDENTIFIER = {$bundleId};\n"
            ."\t\t\t\tPRODUCT_NAME = \"$(TARGET_NAME)\";\n"
            ."\t\t\t\tSKIP_INSTALL = YES;\n"
            ."\t\t\t\tSWIFT_EMIT_LOC_STRINGS = YES;\n"
            ."\t\t\t\tSWIFT_VERSION = 5.0;\n"
            ."\t\t\t\tTARGETED_DEVICE_FAMILY = \"1,2\";\n"
            ."\t\t\t\tWIDGET_EXTENSION_KIND = NativePHPWidget;\n"
            ."\t\t\t};\n"
            ."\t\t\tname = Release;\n"
            ."\t\t};\n"
