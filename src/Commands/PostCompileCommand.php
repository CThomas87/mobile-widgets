            '/* End PBXContainerItemProxy section */',
            "\t\t{$ids['containerProxy']} /* PBXContainerItemProxy */ = {\n"
            ."\t\t\tisa = PBXContainerItemProxy;\n"
            ."\t\t\tcontainerPortal = {$projectObjectId} /* Project object */;\n"
            ."\t\t\tproxyType = 1;\n"
            ."\t\t\tremoteGlobalIDString = {$ids['target']};\n"
            ."\t\t\tremoteInfo = {$targetName};\n"
            ."\t\t};\n"
        );

        if ($result === null) {
            return null;
        }

        $content = $result;

        $result = $this->insertBeforeMarker(
            $content,
            '/* End PBXFileReference section */',
            "\t\t{$ids['fileRef']} /* {$appexName} */ = {isa = PBXFileReference; explicitFileType = \"wrapper.app-extension\"; includeInIndex = 0; path = {$appexName}; sourceTree = BUILT_PRODUCTS_DIR; };\n"
        );

        if ($result === null) {
            return null;
        }

        $content = $result;

        $result = $this->insertBeforeMarker(
            $content,
            '/* End PBXFileSystemSynchronizedRootGroup section */',
            "\t\t{$ids['syncGroup']} /* WidgetsExtension */ = {\n"
            ."\t\t\tisa = PBXFileSystemSynchronizedRootGroup;\n"
            ."\t\t\tpath = WidgetsExtension;\n"
            ."\t\t\tsourceTree = \"<group>\";\n"
            ."\t\t};\n"
        );

        if ($result === null) {
            return null;
        }

        $content = $result;

        if (! str_contains($content, '/* Begin PBXCopyFilesBuildPhase section */')) {
            $copyFilesSection = "/* Begin PBXCopyFilesBuildPhase section */\n/* End PBXCopyFilesBuildPhase section */\n\n";
            $content = preg_replace('/\/\* Begin PBXFileReference section \*\//', $copyFilesSection.'/* Begin PBXFileReference section */', $content, 1) ?? $content;
        }

        $result = $this->insertBeforeMarker(
            $content,
            '/* End PBXCopyFilesBuildPhase section */',
