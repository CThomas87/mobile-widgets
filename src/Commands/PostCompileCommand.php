            'targetDependency' => $this->objectId('target-dependency'),
            'target' => $this->objectId('native-target'),
            'debugConfig' => $this->objectId('debug-config'),
            'releaseConfig' => $this->objectId('release-config'),
            'configList' => $this->objectId('config-list'),
