        if ($result === null) {
            return null;
        }

        $content = $result;

        $result = $this->insertBeforeMarker(
            $content,
            '/* End XCConfigurationList section */',
            "\t\t{$ids['configList']} /* Build configuration list for PBXNativeTarget \"{$targetName}\" */ = {\n"
            ."\t\t\tisa = XCConfigurationList;\n"
            ."\t\t\tbuildConfigurations = (\n"
            ."\t\t\t\t{$ids['debugConfig']} /* Debug */,\n"
            ."\t\t\t\t{$ids['releaseConfig']} /* Release */,\n"
            ."\t\t\t);\n"
            ."\t\t\tdefaultConfigurationIsVisible = 0;\n"
            ."\t\t\tdefaultConfigurationName = Release;\n"
            ."\t\t};\n"
        );

        if ($result === null) {
            return null;
        }

        return $this->insertBeforeMarker(
            $result,
            'rootObject =',
            "\t/* ".self::AUTOGEN_MARKER." */\n"
        );
    }

    private function addLineToNamedGroupChildren(string $content, string $groupName, string $line): ?string
    {
        $groupName = preg_quote($groupName, '/');
        $pattern = "/(\s+[A-F0-9]{24} \/\\* {$groupName} \\*\/ = \{\n.*?\n\s+children = \(\n)(.*?)(\s+\);\n)/s";

        $updated = preg_replace_callback($pattern, function (array $matches) use ($line) {
            if (str_contains($matches[2], trim($line))) {
                return $matches[0];
            }

            return $matches[1].$matches[2].$line.$matches[3];
        }, $content, 1, $count);

        return $count > 0 ? $updated : null;
    }

    private function addLineToNamedTargetList(string $content, string $targetName, string $listName, string $line): ?string
    {
        $targetName = preg_quote($targetName, '/');
        $listName = preg_quote($listName, '/');
        $pattern = "/(\s+[A-F0-9]{24} \/\\* {$targetName} \\*\/ = \{\n.*?\n\s+{$listName} = \(\n)(.*?)(\s+\);\n)/s";

        $updated = preg_replace_callback($pattern, function (array $matches) use ($line) {
            if (str_contains($matches[2], trim($line))) {
                return $matches[0];
            }

            return $matches[1].$matches[2].$line.$matches[3];
        }, $content, 1, $count);

        return $count > 0 ? $updated : null;
    }

    private function addLineToProjectTargets(string $content, string $line): ?string
    {
        $pattern = "/(\s+[A-F0-9]{24} \/\\* Project object \\*\/ = \{\n.*?\n\s+targets = \(\n)(.*?)(\s+\);\n)/s";

        $updated = preg_replace_callback($pattern, function (array $matches) use ($line) {
            if (str_contains($matches[2], trim($line))) {
                return $matches[0];
            }

            return $matches[1].$matches[2].$line.$matches[3];
        }, $content, 1, $count);

        return $count > 0 ? $updated : null;
    }

    private function insertBeforeMarker(string $content, string $marker, string $insert): ?string
    {
        $position = strpos($content, $marker);

        if ($position === false) {
            return null;
        }

        return substr($content, 0, $position).$insert.substr($content, $position);
    }

    private function detectProjectObjectId(string $content): ?string
    {
        if (preg_match('/^\s*([A-F0-9]{24}) \/\* Project object \*\/ = \{/m', $content, $matches)) {
            return $matches[1];
        }

        return null;
    }

    private function detectMainTargetName(string $content): ?string
    {
        if (! preg_match_all('/\s+([A-F0-9]{24}) \/\* ([^*]+) \*\/ = \{\n(.*?)\s+\};\n/s', $content, $blocks, PREG_SET_ORDER)) {
            return null;
        }

        foreach ($blocks as $block) {
            $body = $block[3];

            if (! str_contains($body, 'isa = PBXNativeTarget;')) {
                continue;
            }

            if (str_contains($body, 'productType = "com.apple.product-type.application";')) {
                return trim($block[2]);
            }
        }

        return null;
    }

    private function objectId(string $seed): string
    {
        return strtoupper(substr(md5('mobile-widgets-'.$this->appId().'-'.$seed), 0, 24));
    }
}
