    private const AUTOGEN_MARKER = 'NATIVEPHP_WIDGETS_AUTOGENERATED';

    private const TARGET_NAME = 'NativePHPWidgetsExtension';

    protected $signature = 'nativephp:widgets:post-compile';

    protected $description = 'Post-compile tasks for Widgets plugin';

    public function handle(): int
    {
        if (! $this->isIos()) {
            return self::SUCCESS;
        }

        $pbxprojPath = $this->locatePbxprojPath();

        if (! $pbxprojPath) {
            $this->warn('No iOS project.pbxproj found in build output; skipping WidgetKit target injection.');

            return self::SUCCESS;
        }

        $content = file_get_contents($pbxprojPath);

        if ($content === false) {
            $this->warn('Unable to read iOS project.pbxproj; skipping WidgetKit target injection.');

            return self::SUCCESS;
        }

        if (str_contains($content, self::AUTOGEN_MARKER) || str_contains($content, '/* '.self::TARGET_NAME.' */ = {')) {
            $this->info('WidgetKit target already present in iOS project.');

            return self::SUCCESS;
        }

        $updated = $this->injectWidgetTarget($content);

        if ($updated === null) {
            $this->warn('Unable to inject WidgetKit target automatically; project format did not match expected anchors.');

            return self::SUCCESS;
        }

        if (file_put_contents($pbxprojPath, $updated) === false) {
            $this->warn('Failed writing updated iOS project.pbxproj; WidgetKit target injection was not saved.');

            return self::SUCCESS;
        }

        $this->info('Injected WidgetKit app extension target into iOS project.pbxproj.');

        return self::SUCCESS;
    }

    private function locatePbxprojPath(): ?string
    {
        $buildPath = $this->buildPath();

        if (! is_dir($buildPath)) {
            return null;
        }

        $direct = glob($buildPath.'/*.xcodeproj/project.pbxproj');

        if (is_array($direct) && count($direct) > 0) {
            return $direct[0];
        }

        $iterator = new RecursiveIteratorIterator(
            new RecursiveDirectoryIterator($buildPath, RecursiveDirectoryIterator::SKIP_DOTS)
        );

        foreach ($iterator as $file) {
            if (! $file->isFile() || $file->getFilename() !== 'project.pbxproj') {
                continue;
            }

            $parent = dirname((string) $file->getPathname());

            if (str_ends_with($parent, '.xcodeproj')) {
                return (string) $file->getPathname();
            }
        }

        return null;
    }

    private function injectWidgetTarget(string $content): ?string
